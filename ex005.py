#Antecessor e Sucessor
n1 = int(input('Escolha um número aleatório: '))
a = (n1 - 1)
s = (n1 + 1)
print('O antecessor de {0} é {1} e o seu sucessor é {2}'.format(n1, a, s))

#Mesmo exercício com menos variáveis

